void EXTI0_IRQHandler(void)
{
	EXTI_InitTypeDef EXTI_InitStructure;
	
  if (EXTI_GetITStatus(EXTI_Line0) != RESET) // 判断是否发生中断
  {
    EXTI_ClearITPendingBit(EXTI_Line0); // 清除中断标志位
		if(GPIO_ReadInputDataBit(GPIOB,GPIO_Pin_0) == Bit_SET)
		{
			GPIO_EXTILineConfig(GPIO_PortSourceGPIOB, GPIO_PinSource0);

			EXTI_InitStructure.EXTI_Line = EXTI_Line0;
			EXTI_InitStructure.EXTI_Mode = EXTI_Mode_Interrupt;
			EXTI_InitStructure.EXTI_Trigger = EXTI_Trigger_Falling;
			EXTI_InitStructure.EXTI_LineCmd = ENABLE;
			EXTI_Init(&EXTI_InitStructure);
			LED1_OFF;
			LCD_WriteString(30,130, LCD_COLOR_BLUE, LCD_COLOR_WHITE, (uint8_t *)"state: Normal"); 
		}
		else
		{
			EXTI_InitStructure.EXTI_Line = EXTI_Line0;
			EXTI_InitStructure.EXTI_Mode = EXTI_Mode_Interrupt;
			EXTI_InitStructure.EXTI_Trigger = EXTI_Trigger_Rising;
			EXTI_InitStructure.EXTI_LineCmd = ENABLE;
			EXTI_Init(&EXTI_InitStructure);
			LED1_ON;
			LCD_WriteString(30,130, LCD_COLOR_BLUE, LCD_COLOR_WHITE, (uint8_t *)"state: ALARM "); 
		}
  }
}

